<script>
        document.addEventListener('DOMContentLoaded', function() {
            // Get the sidebar toggle button
            var sidebarToggle = document.querySelector('[data-drawer-toggle="logo-sidebar"]');
            
            // Get the sidebar element
            var sidebar = document.getElementById('logo-sidebar');

            // Add click event listener to the toggle button
            sidebarToggle.addEventListener('click', function() {
                // Toggle the class to show/hide the sidebar
                sidebar.classList.toggle('translate-x-0');
                sidebar.classList.toggle('-translate-x-full');
            });
        });
    </script>

<script>
  document.addEventListener('DOMContentLoaded', function() {
    const dropdownUserButton = document.getElementById('dropdownUserButton');
    const dropdownUserMenu = document.getElementById('dropdownUserMenu');

    dropdownUserButton.addEventListener('click', function() {
      dropdownUserMenu.classList.toggle('hidden');
    });

    window.addEventListener('click', function(e) {
      if (!dropdownUserButton.contains(e.target) && !dropdownUserMenu.contains(e.target)) {
        dropdownUserMenu.classList.add('hidden');
      }
    });
  });
</script>
